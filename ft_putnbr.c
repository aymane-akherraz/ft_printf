#include "ft_printf.h"

void	ft_putnbr(int n, int *p)
{
	char	c;

	if (n == -2147483648)
	{
		*p += write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		*p += write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr((n / 10), p);
	c = (n % 10) + '0';
	*p += write(1, &c, 1);
}
